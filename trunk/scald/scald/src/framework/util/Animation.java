package framework.util;

public class Animation {

	private String name;
	
	private int begin;
	
	private int end;

	/**
	 * Constructor of the class
	 */
	public Animation(){
		
	}
	
	public int getBegin() {
		return begin;
	}

	public void setBegin(int begin) {
		this.begin = begin;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
